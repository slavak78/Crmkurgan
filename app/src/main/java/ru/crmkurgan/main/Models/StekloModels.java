package ru.crmkurgan.main.Models;

public class StekloModels {

        private String StekloId;
        private String StekloName;
        private int StekloSelected;

        public String getStekloId() {
            return StekloId;
        }

        public void setStekloId(String id) {
            this.StekloId = id;
        }

        public String getStekloName() {
            return StekloName;
        }

        public void setStekloName(String name) {
            this.StekloName = name;
        }

    public int getStekloSelected() {
        return StekloSelected;
    }

    public void setStekloSelected(int selected) {
        this.StekloSelected = selected;
    }
    }
