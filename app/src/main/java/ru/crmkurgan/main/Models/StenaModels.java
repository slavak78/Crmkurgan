package ru.crmkurgan.main.Models;

public class StenaModels {

        private String StenaId;
        private String StenaName;
        private int StenaSelected;

        public String getStenaId() {
            return StenaId;
        }

        public void setStenaId(String id) {
            this.StenaId = id;
        }

        public String getStenaName() {
            return StenaName;
        }

        public void setStenaName(String name) {
            this.StenaName = name;
        }

    public int getStenaSelected() {
        return StenaSelected;
    }

    public void setStenaSelected(int selected) {
        this.StenaSelected = selected;
    }
    }
