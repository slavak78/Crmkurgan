package ru.crmkurgan.main.Models;

public class WindModels {

        private String WindId;
        private String WindName;
        private int WindSelected;

        public String getWindId() {
            return WindId;
        }

        public void setWindId(String id) {
            this.WindId = id;
        }

        public String getWindName() {
            return WindName;
        }

        public void setWindName(String name) {
            this.WindName = name;
        }

    public int getWindSelected() {
        return WindSelected;
    }

    public void setWindSelected(int selected) {
        this.WindSelected = selected;
    }
    }
